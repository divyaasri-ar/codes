public class EmployeeInheritance
{
	int eid;
	String eName;
	long sal;
public EmployeeInheritance(int eid,String eName,long sal)
{
	this.eid=eid;
	this.eName=eName;
	this.sal=sal;
}
public void setEid(int id)
{
	eid=id;
}
public int getEid()
{
	return eid;
}
public void setEName(String name)
{
	eName=name;
}
public String getEName()
{
	return eName;
}
public void setSal(long salary)
{
	sal=salary;
}
public long getSal()
{
	return sal;
}
}









	